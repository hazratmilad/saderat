        -1000 => 'خطای ارتباط با بانک',
        // ---------------------------------------------------------
        1     => 'وجود خطا در فرمت اطلاعات ارسالی',
        2     => 'عدم وجود پذیرنده و ترمینال مورد درخواست در سیستم',
